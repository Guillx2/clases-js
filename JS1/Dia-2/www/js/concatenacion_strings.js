'use strict';

/* Se pueden concatenar strings de la siguiente manera: */
 
/* Hay que tener en cuenta que al concatenar strings, no se respetan los espacios 
entre palabras. Debemos añadirlos manualmente  */

const texto_1 = 'Guille '; // Podemos añadir aquí el espacio (opción 1)
const texto_2 = 'está estudiando';
const texto_3 = 'JavaScript';

// O podemos añadir un espacio concatenando un string: (' ')
const texto = texto_1 + texto_2 + ' ' + texto_3; 

console.log(texto);




